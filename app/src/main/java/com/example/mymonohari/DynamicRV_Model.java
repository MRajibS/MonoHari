package com.example.mymonohari;

public class DynamicRV_Model {
    private int img;
    private String text;

    public DynamicRV_Model(int img, String text) {
        this.img = img;
        this.text = text;
    }

    public int getImg() {
        return img;
    }

    public String getText() {
        return text;
    }
}
